<h1 id="h1pg2">Development of jazz clubs in Townsville</h1>
        <p id="p1">Whilst jazz music had been played in Townsville long before the Harbour End Jazz Club was formed in 1980, there had not been a recognised venue where jazz was performed for about twenty years. This club effectively started the revival of the jazz scene in Townsville which was then carried on with the formation of the North Queensland Jazz Club (NQ Jazz Club Inc. from 1995) and its subsequent change of name to Townsville Jazz Club Inc. in 2012. The Harbour End Jazz Club was started in 1980 by Mo Murakami, Ted Redknap, Bob Hebden and Les Nicholson.<br><br>Ted and Mo were members of the widely acclaimed North Qld Area Army Band. The Army band had a close association with the local musicians and having Ted and Mo as Directors cemented that close relationship. Mo also had an input into starting the NQ Jazz Club which still exists today and is a significant institution in the promotion of jazz in Townsville.<br><br>The club met on every second Thursday at the then Tattersalls Hotel (Tatts) in a back room that was small but very intimate, the ideal jazz club.  Crowds were phenomenal and there was not enough room to raise your arm to drink your beer.  The room spilled out to a beer garden which thankfully provided some space. Brian Fitzimmons and his wife Pat ran Tatts and were very supportive of the club.<br><br>Brian and Pat also ran jazz in the beer garden on Saturday afternoons which provided work for the local bands. Great musos such as Billy Laut, Ross Collins, Kanga Yoman and Franz Condie were regulars. All were ex Sydney musos who had come north to get away from the rat race.</p>
